    Children are defined as people 18 years or younger. Doctors in the model are 70 years or younger. Clinical workforce equivalent (CWE) refers to
    the headcount adjusted by the proportion of time spent in direct clinical or consultative
    care. This value is self-reported in response to the question "What proportion of your total
    professional time is spent performing each of the following tasks?" The proportion used for
    this calculation is from the following category: "Direct and/or consultative inpatient and
    outpatient care, including patient billing and charting (with or without trainees)."
